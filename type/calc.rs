fn main() {
    let sum = 5 + 10; // 加
    println!("sum {}", sum);
    let difference = 95.5 - 4.3; // 减
    println!("difference {}", difference);
    let product = 4 * 30; // 乘
    println!("product {}", product);
    let quotient = 56.7 / 32.2; // 除
    println!("quotient {}", quotient);
    let remainder = 43 % 5; // 求余
    println!("remainder {}", remainder);
}